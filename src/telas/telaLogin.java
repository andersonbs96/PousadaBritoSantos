    public void Logar(){
        String sql = "SELECT * FROM tabela_admin WHERE admin_login=? AND admin_senha=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, inputLogin.getText());
            pst.setString(2, inputSenha.getText());
            
            rs = pst.executeQuery();
            
            if (rs.next()){
                telaPrincipal principal = new telaPrincipal();
                principal.setVisible(true);
                this.dispose();
                
                conexao.close();
            }
            else {
                JOptionPane.showMessageDialog(null, "Usuários e/ou Senha inválidos");
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void Cancelar(){
        int sair = JOptionPane.showConfirmDialog(null, "Você deseja mesmo cancelar?", "Atenção", JOptionPane.YES_NO_OPTION);
        
        if (sair == JOptionPane.YES_OPTION){
            telaInicial inicial = new telaInicial();
            inicial.setVisible(true);
            this.dispose();
        }
        
    }
    
